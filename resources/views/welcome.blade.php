                  El bullying, o acoso escolar, es un comportamiento repetitivo e intencional de dañar a otra persona a través de agresiones físicas, verbales o sociales. Puede ocurrir en diversos entornos, como la escuela, el trabajo o en línea.
                  </div>
                </div>
              </div><!-- # Faq item-->

