                  <div class="accordion-body">
                  Escucha con empatía, ofrece tu apoyo y alienta a la persona a hablar con un adulto de confianza, como un profesor o un padre. Es importante tomar en serio cualquier indicio de acoso y reportarlo a las autoridades correspondientes.
                  </div>
                </div>
