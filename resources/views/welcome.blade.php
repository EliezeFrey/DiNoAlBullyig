                  </button>
                </h3>
                <div id="faq-content-2" class="accordion-collapse collapse" data-bs-parent="#faqlist">
                  <div class="accordion-body">
                  Existen varios tipos de bullying, como el verbal (insultos, burlas), el social (aislamiento, exclusión), el físico (agresiones físicas), y el ciberacoso (acoso a través de medios electrónicos).
                </div>
              </div><!-- # Faq item-->

              <div class="accordion-item">
                <h3 class="accordion-header">
                  <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#faq-content-3">
                    <span class="num">3.</span>
                    ¿Cuáles son las señales de que alguien está siendo víctima de bullying?
                  </button>
                </h3>
                <div id="faq-content-3" class="accordion-collapse collapse" data-bs-parent="#faqlist">
                  <div class="accordion-body">
                  Cambios en el comportamiento, pérdida de interés en actividades antes disfrutadas, disminución del rendimiento académico, problemas de salud no explicados y evitación de situaciones sociales pueden ser señales de que alguien está siendo víctima de bullying.
                  </div>
                </div>
              </div><!-- # Faq item-->

              <div class="accordion-item">
                <h3 class="accordion-header">
                  <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#faq-content-4">
